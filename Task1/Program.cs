// Создание массива на 5 чисел:
int[] arrey = new int[5];
arrey[0] = 3;
arrey[1] = 4;
arrey[2] = 7;
arrey[3] = 9;
arrey[4] = 1;
Console.WriteLine(arrey[0]);
Console.WriteLine(arrey[1]);
Console.WriteLine(arrey[2]);
Console.WriteLine(arrey[3]);
Console.WriteLine(arrey[4]);

// Присвоить значения элементам индекса необходимо через фигурные скобки {}
int[] arrey = [5]{1, 2, 3, 4, 5};

// Создание массива возможно без команды new int[]. Пишем просто:
int[] arrey = {1, 2, 3, 4, 5};